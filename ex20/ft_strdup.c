#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;
	int		len;

	len = 0;
	while (src[len])
		len++;
	str = (char*)malloc(sizeof(*str) * (len + 1));
	i = 0;
	while (i < len)
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
